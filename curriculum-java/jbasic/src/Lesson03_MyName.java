
// クラス名はファイル名と同じにすること
// クラス名は大文字始まり
public class Lesson03_MyName {

	// プログラムの実行は main メソッドから始まる
	public static void main(String[] args) {

		// 文字列を改行つきで出力
		// println() メソッドは System.out クラスのメソッド
		// println() メソッドは改行を含む
		System.out.println("山田太郎です。");
		System.out.println("これから Java をがんばります。");
	}
}

