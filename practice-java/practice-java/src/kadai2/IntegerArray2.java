// このプログラムは整数の配列の合計を計算するプログラムです

package kadai2;

// IntegerArray2というクラスを定義
public class IntegerArray2 {

	// プログラムの実行が始まる場所（メインメソッド）を定義
	public static void main(String[] args) {

		// 整数の配列を定義し、初期値を設定します
		// この配列には1, 7, 2, 7, 3という5つの数値が格納
		// int[]は整数型の配列を表し、{}内に初期値を指定
		int[] numbers = { 1, 7, 2, 7, 3 };

		// 合計値を保存するための変数を定義し、初期値0を設定
		int sum = 0;

		// 拡張for文を使用して配列の各要素にアクセス
		// numには配列の各要素が順番に代入
		for (int num : numbers) {

			// 現在の合計値に配列の要素を加算
			// +=演算子は、変数に値を加算後にその結果を再代入するために使用
			// sumが1でnumが7の場合、sumは8になる
			sum += num;
		}

		// 計算結果を画面に表示
		System.out.println("合計：" + sum);
	}
}

// 参考：拡張for文の文法

// 左の部分は、右の配列やリストの要素を順番に取り出すための構文
// だからnumの様な単数系の変数名で、右のたくさん入ったデータの集まりから一つずつ取り出している
