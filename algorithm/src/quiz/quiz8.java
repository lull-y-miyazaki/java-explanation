package quiz;

/**
 * Step1:
 * for文を使って1～9までの数字を横に並べなさい
 * 数字の後ろには半角スペースを入れます
 * ---
 * 1 2 3 4 5 6 7 8 9 
 * ---
 * Step2:
 * for文の中でfor文を使って九九表を作成しなさい
 * ---
 * 1 2 3 4 5 6 7 8 9 
 * 2 4 6 8 10 12 14 16 18 
 * 3 6 9 12 15 18 21 24 27 
 * 4 8 12 16 20 24 28 32 36 
 * 5 10 15 20 25 30 35 40 45 
 * 6 12 18 24 30 36 42 48 54 
 * 7 14 21 28 35 42 49 56 63 
 * 8 16 24 32 40 48 56 64 72 
 * 9 18 27 36 45 54 63 72 81 
 * ---
 * Step3:
 * 九九表のレイアウトを綺麗にしなさい
 * ---
 *  1  2  3  4  5  6  7  8  9 
 *  2  4  6  8 10 12 14 16 18 
 *  3  6  9 12 15 18 21 24 27 
 *  4  8 12 16 20 24 28 32 36 
 *  5 10 15 20 25 30 35 40 45 
 *  6 12 18 24 30 36 42 48 54 
 *  7 14 21 28 35 42 49 56 63 
 *  8 16 24 32 40 48 56 64 72 
 *  9 18 27 36 45 54 63 72 81 
 */

public class quiz8 {

	public static void main(String[] args) {
		
		//step1
		// 1を9まで1ずつ増やして（インクリメントして）、1から9までの数字を横に並べる
		for (int i = 1; i <= 9; i++) {
			System.out.print(i + " "); // iの値（数字）を表示し、続けてスペースも入れる（printなので改行しない）
		}

		//step2
		// 外側のfor文：1の段〜9の段まで繰り返す（行を表す）
		for (int j = 1; j <= 9; j++) {

			// 内側のfor文：1〜9まで繰り返して、掛け算の結果を表示する（列を表す）
			for (int i = 1; i <= 9; i++) {
				System.out.print(i * j + " "); // i × j の結果を表示（横に並べる）
			}

			System.out.println(); // 1段分の表示が終わったら、改行して次の段へ
		}

		//step3
		// 外側のfor文：段（1の段〜9の段）を表す → 行をつくる
		for (int j = 1; j <= 9; j++) {

			// 内側のfor文：掛ける数（1〜9）を表す → 列をつくる
			for (int i = 1; i <= 9; i++) {

				// 掛け算の結果が1桁（10より小さい）なら、スペースを1つ追加
				// → 表の桁をそろえるための工夫
				if (i * j < 10) {
					System.out.print(" "); // 1桁のときだけスペースを追加する
				}

				// 掛け算の結果を表示（横に並べる）
				System.out.print(i * j + " ");
			}

			// 1段分（1行分）の表示が終わったら、改行して次の段へ
			System.out.println();
		}

	}

}
